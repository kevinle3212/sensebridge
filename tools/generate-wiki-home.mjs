#!/usr/bin/env node
// Generates the GitHub Wiki Home.md from WIKI.md, rewriting repo-relative
// `path.md` code spans into absolute blob links — the wiki is a separate git
// repository (owner/repo.wiki.git), so a relative link would 404 there.
// Stdlib-only, no dependencies. Prints to stdout; the wiki-sync workflow
// redirects that into wiki/Home.md.

import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const REPO = "kevinle3212/sensebridge";
const BRANCH = "main";

const wikiPath = fileURLToPath(new URL("../WIKI.md", import.meta.url));
const wiki = readFileSync(wikiPath, "utf8");

const linked = wiki.replace(/`([A-Za-z0-9_.-]+(?:\/[A-Za-z0-9_.-]+)*\.md)`/g, (_match, path) => {
  const url = `https://github.com/${REPO}/blob/${BRANCH}/${path}`;
  return `[\`${path}\`](${url})`;
});

const header =
  "<!-- Generated by tools/generate-wiki-home.mjs from WIKI.md. " +
  "Do not edit this page directly — edit WIKI.md on main and " +
  ".github/workflows/wiki-sync.yml will regenerate it. -->\n\n";

process.stdout.write(header + linked);
